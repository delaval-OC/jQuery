// to add element before a element tag
$("h1").before("<button>beforeButton</button>");

// to add element after a element tag
$("h1").after("<button>afterButton</button>");

//to add after but in  element tag
$("h1").append("<button>append</button>");

//to add before but in element tag
$("h1").prepend("<button>prepend</button>");

